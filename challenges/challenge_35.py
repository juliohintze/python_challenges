# Get two numbers from the user. Print if the first number is greater than, less than or equals the second number.

num1 = input("gimme a number\n")
num2 = input("yeaaaa ok yea that's pretty cool but gimme another\n")

if num1 > num2:
    print(num1, ">", num2)
if num1 < num2:
    print(num1, "<", num2)
if num1 == num2:
    print(num1, "=", num2)